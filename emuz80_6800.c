//    //6800 memory read cycle
//        TRISC = 0x00; // Set data bus as output
//            if((ab.w >= ROM_TOP) && (ab.w <= ROM_END)) // ROM area
//            LATC = rom[ab.w - ROM_TOP]; // Out ROM data
//        else if((ab.w >= RAM_TOP) && (ab.w <= RAM_END)) // RAM area
//            LATC = ram[ab.w - RAM_TOP]; // Out RAM data
//        else if((ab.w == UART_CREG) && (RA2)) // PIR9
//            LATC = PIR9; // Out PIR9
